<p>
The <a href="https://www.facebook.com/AssociationMountainboardCompiegne/" target="_blank">AMC, Compiègne’s mountainboarding club</a> and the <a href="https://www.mountainboardworld.org/" target="_blank">International Mountainboarding Association</a> are proud to present the 2017 Mountainboard World Championships!
</p>
<p>
The competition will be held in Venette, France (near Compiègne, in the Oise region) from <strong>July 13th to the 15th</strong>, and for the first time, we’ll have both <strong>Boardercross and Freestyle</strong> at the same time!
</p>
<p>
We’ll have 4 categories: Junior, Ladies, Masters and Open.
</p>
<p>
This year’s event promises to be exciting! Will someone steal the podium from Matt Brind who won the last 3 editions of the Boardercross?<br>
As for Freestyle, well there hasn’t been a World Freestyle Championship in several years, so riders from all over the world will be eager to show their skills and new tricks.
</p>
<p>
With a very accessible track, riders of all levels are guaranteed to have a good time, so we invite you to <a href="<?php echo BASE_URL ?>registration">join us and register early!</a>
