<h2>Boardercross</h2>
<p>The boardercross course will be modified for this event and was made longer with an emphasis on making berms wider to encourage passing maneuvers.</p>
<p>
	<img class="fillContainer" src="<?php echo BASE_URL ?>images/boardercross.jpg" alt="Photo of the boardercross track for the Mountainboard World Championships">
</p>
<p>This is a 4-person race course, and it is very different from the one in Novi Sad, Serbia, which hosted the last few boardercross championships. <br>
The Serbian track is famous for being really fast, and it repeatedly tests riders&#8217; confidence and ability to handle speed, whereas the course in Venette is less fast but has a lot of features like rollers and table tops, meaning it is accessible to all riders and will also provide lots of opportunities for overtaking.<br>
Expect a lot of close races and spectacular maneuvers!</p>