<h1>Schedule</h1>

<p>Qualifiers will run on Thursday for both Freestyle and Boardercross (timed runs only). Friday will see the Freestyle finals as well as Boardercross races. The Boardercross finals will be held on Saturday.</p>

<h2>Thursday July 13th</h2>
<p>14h-18h: Boardercross timed runs and freestyle qualifiers (three jumps) will run in parallel. During this 4-hour window, riders can ride when they choose.</p>

<h2>Friday July 14th</h2>
<p>14h-18h: Boardercross qualifiers (4-person races).</p>
<p>18h-21h: Freestyle finals (all categories but pro).</p>
<p>22h-00h: Pro freestyle finals for the top 15 riders.</p>

<h2>Saturday July 15th</h2>
<p>14h-18h: Boardercross finals.</p>
<p>19h: Prize ceremony, concert.</p>